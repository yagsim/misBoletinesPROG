    private void botonPuntoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonPuntoActionPerformed
        String cadena;
        cadena =textoMuestra.getText();
        if(!cadena.contains(".")){
        textoMuestra.setText(textoMuestra.getText()+".");
        }
        if(cadena.length()<=0){
            textoMuestra.setText("0.");
        }else{
        if(!existePunto(textoMuestra.getText())){
            textoMuestra.setText(textoMuestra.getText()+".");
        }
        }
        
        
    }//GEN-LAST:event_botonPuntoActionPerformed

    public static boolean existePunto(String cadena){
        boolean resultado;
        resultado=false;
        for(int i=0; i < cadena.length() ; i++){
            if(cadena.substring(i , i+1).equals("."));//compara uno por uno la cadena
            resultado=true; //ya encontro un punto por lo tanto no puede haber otro
            break;
        }
        return resultado;
        
    }
    

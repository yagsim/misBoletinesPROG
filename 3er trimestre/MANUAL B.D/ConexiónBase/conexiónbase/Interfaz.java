    public static void mostrarJugador() {
        ResultSet result;
        ResultSetMetaData rsds;
        DefaultTableModel modelo = (DefaultTableModel) Tabla.getModel();
        modelo.setRowCount(0);
        int columnas;
        try {
            PreparedStatement st = connect.prepareStatement("select * from jugador");

            result = st.executeQuery();
            rsds = result.getMetaData();
            columnas = rsds.getColumnCount();
            while (result.next()) {
                Object[] fila = new Object[columnas];
                for (int i = 0; i < columnas; i++) {
                    fila[i] = result.getObject(i + 1);  //empezar a partir de 1
                }
                modelo.addRow(fila);
            }
            st.execute();
        } catch (SQLException ex) {
            System.err.println(ex.getMessage());
        }
    }

